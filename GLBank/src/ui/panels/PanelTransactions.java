    private void txtAmountNonDecimalPartKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAmountNonDecimalPartKeyTyped
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c) || (c==KeyEvent.VK_BACK_SPACE) || c==KeyEvent.VK_DELETE)) {
            getToolkit().beep();
            evt.consume();
        }
        if (txtAmountNonDecimalPart.getText().length() >= 14 ) // limit textfield to 4 characters
            evt.consume();
    }//GEN-LAST:event_txtAmountNonDecimalPartKeyTyped

